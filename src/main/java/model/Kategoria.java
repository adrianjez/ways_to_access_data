package model;

public class Kategoria {
	
	private int id;
	private String nazwa;
	private String PEGI;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNazwa() {
		return nazwa;
	}
	public void setNazwa(String nazwa) {
		this.nazwa = nazwa;
	}
	public String getPEGI() {
		return PEGI;
	}
	public void setPEGI(String pEGI) {
		PEGI = pEGI;
	}
	
}
